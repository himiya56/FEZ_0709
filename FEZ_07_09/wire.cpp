#include "wire.h"

//========================================
// 静的メンバ変数宣言
//========================================
LPDIRECT3DTEXTURE9 CWire::m_pTexture = NULL;

//=============================================================================
// コンストラクタ
//=============================================================================
CWire::CWire()
{
	m_pos = DEFAULT_VECTOR;
	wire = WIRE_NONE;
	m_bShoot = false;
}

//=============================================================================
// デストラクタ
//=============================================================================
CWire::~CWire()
{
}

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT CWire::Init(void)
{
	CBillboard::Init();

	m_pos = DEFAULT_VECTOR;
	m_rot = DEFAULT_VECTOR;
	wire = WIRE_NONE;

	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void CWire::Uninit(void)
{
	CBillboard::Uninit();
}

//=============================================================================
// 更新処理
//=============================================================================
void CWire::Update(void)
{
	CBillboard::Update();
}

//=============================================================================
// 描画処理
//=============================================================================
void CWire::Draw(void)
{
	CBillboard::Draw();
}

//=============================================================================
// テクスチャ読み込み
//=============================================================================
void CWire::Load(void)
{
}

//=============================================================================
// テクスチャ破棄
//=============================================================================
void CWire::Unload(void)
{
	if (m_pTexture != NULL)
	{
		m_pTexture->Release();
		m_pTexture = NULL;
	}
}

//=============================================================================
// インスタンス生成処理
//=============================================================================
CWire * CWire::Create(D3DXVECTOR3 pos)
{
	CWire *pWire = NULL;
	pWire = new CWire;
	pWire->Init();
	pWire->SetPos(pos);
	return pWire;
}
