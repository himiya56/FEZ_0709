CStartButton::~CStartButton()
{
}

//=============================================================================
// テクスチャ読み込み関数
//=============================================================================
HRESULT CStartButton::TextureLoad(void)
{
	//レンダラーの取得
	CRenderer *pRenderer = CManager::GetRenderer();
	//デバイスの取得
	LPDIRECT3DDEVICE9 pDevice = pRenderer->GetDevice();
	//テクスチャの生成
	D3DXCreateTextureFromFile(pDevice,	//デバイスへのポインタ
		TEXTURE_PASS,					//ファイルの名前
		&m_pTexture);					//読み込むメモリー
	return S_OK;
}

//=============================================================================
// テクスチャ破棄関数
//=============================================================================
void CStartButton::TextureUnload(void)
{
	//もしテクスチャのポインタがNULLではない場合
	if (m_pTexture != NULL)
	{
		//テクスチャの破棄処理関数呼び出し
		m_pTexture->Release();
		//テクスチャをNULLにする
		m_pTexture = NULL;
	}
}

//=============================================================================
// 生成処理関数呼び出し
//=============================================================================
CStartButton * CStartButton::Create(D3DXVECTOR3 Position)
{
	//スタートボタンのポインタ
	CStartButton * pStartButton = NULL;
	//スタートボタンのポインタがNULLの場合
	if (pStartButton == NULL)
	{
		//スタートボタンのメモリ確保
		pStartButton = new CStartButton;
		//スタートボタンのポインタがNULLではない場合
		if (pStartButton != NULL)
		{
			//スタートボタンの位置設定
			pStartButton->SetPos(Position);
			//スタートボタンの初期化処理関数呼び出し
			pStartButton->Init();
		}
	}
	//スタートボタンのポインタを返す
	return pStartButton;
}

//=============================================================================
// 初期化処理関数
//=============================================================================
HRESULT CStartButton::Init(void)
{
	//ボタンの初期化処理関数呼び出し
	CButton::Init();
	//テクスチャの設定
	SetTexUV(0.0f, 1.0f, 0.0f, 1.0f);
	//テクスチャの割り当て
	BindTexture(m_pTexture);
	return S_OK;
}

